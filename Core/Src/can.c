void CAN_Config(void) {
	
	txHeader.StdId = 0x100; // id of transmitter
	txHeader.DLC = 8;
	txHeader.RTR = CAN_RTR_DATA; // send data frame
	txHeader.IDE = CAN_ID_STD;
	
	/* Identifier */
	canFilterConfig.FilterIdHigh = 0x200 << 5;
	canFilterConfig.FilterIdLow = 0;
	
	/* Bitmask */
	canFilterConfig.FilterMaskIdHigh = 0x7F0 << 5;
	canFilterConfig.FilterMaskIdLow = 0;
	
	canFilterConfig.FilterBank = 0;
	canFilterConfig.FilterMode = CAN_FILTERMODE_IDMASK;
	canFilterConfig.FilterScale = CAN_FILTERSCALE_32BIT;
	canFilterConfig.FilterFIFOAssignment = CAN_FILTER_FIFO0;
	canFilterConfig.FilterActivation = ENABLE;
	canFilterConfig.SlaveStartFilterBank = 0;
	HAL_CAN_ConfigFilter(&hcan1, &canFilterConfig);
	
	HAL_CAN_Start(&hcan1); // start CAN module
	HAL_CAN_Start(&hcan2);
	HAL_CAN_ActivateNotification(&hcan1, CAN_IT_RX_FIFO0_MSG_PENDING); // enable rx interrupts
	HAL_CAN_ActivateNotification(&hcan2, CAN_IT_RX_FIFO0_MSG_PENDING); 
	HAL_CAN_ActivateNotification(&hcan1, CAN_IT_TX_MAILBOX_EMPTY); // enable tx interrupts
	HAL_CAN_ActivateNotification(&hcan2, CAN_IT_TX_MAILBOX_EMPTY); 
}

void CAN_Transmit(void) {
	
	for (int i = 0; i < 8; i++) {
		
		txData[i] = i;//rxData[i];
	}
		
	HAL_CAN_AddTxMessage(&hcan1, &txHeader, txData, &txMailBox);
	//HAL_CAN_AddTxMessage(&hcan2, &txHeader, txData, &txMailBox);
}

