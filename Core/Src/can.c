/* Receive data */
void HAL_CAN_RxFifo0MsgPendingCallback(CAN_HandleTypeDef *hcan) { 

  HAL_CAN_GetRxMessage(&hcan1, CAN_RX_FIFO0, &rxHeader, rxData);
    
  if (rxHeader.IDE == CAN_ID_EXT) // ext frame
  {
    rxId = rxHeader.ExtId;
  }
  else // std frame
  {
    rxId = rxHeader.StdId; 
  }

	if (HAL_CAN_GetTxMailboxesFreeLevel(&hcan1) != 0) {
		
		CAN_Transmit();
	}
}
