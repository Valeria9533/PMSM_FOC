void ADC_read(void) {

	// Reading ADC
	i_Sabc_adc.phaseA = ADC1->JDR1;
	i_Sabc_adc.phaseB = ADC2->JDR1;
	i_Sabc_adc.phaseC = ADC3->JDR1;
	power = ADC3->JDR2;

	if(adc_init == 0) {
		if (adc_cnt <= 0) {
			adc[0] = adc[0]/1000;	
			adc[1] = adc[1]/1000;
			adc[2] = adc[2]/1000;		
			adc_init = 1;		
		} 
		else {
			adc[0] += (int32_t)i_Sabc_adc.phaseA;	
			adc[1] += (int32_t)i_Sabc_adc.phaseB;	
			adc[2] += (int32_t)i_Sabc_adc.phaseC;	
			adc_cnt--;
		}
	}
	else {
		i_Sabc.phaseA = (int16_t)adc[0] - i_Sabc_adc.phaseA;
		i_Sabc.phaseB = (int16_t)adc[1] - i_Sabc_adc.phaseB; 
		i_Sabc.phaseC = (int16_t)adc[2] - i_Sabc_adc.phaseC;
		
		// For shunt measure
		switch (sector) { 
			case 3:       
			case 2: i_Sabc.phaseB = - i_Sabc.phaseA - i_Sabc.phaseC; break;
			case 4: 
			case 5: i_Sabc.phaseC = - i_Sabc.phaseA - i_Sabc.phaseB; break;        
			case 1:
			case 6: i_Sabc.phaseA = - i_Sabc.phaseB - i_Sabc.phaseC; break;
		} 	
	}
}

void ADC_callback(void) {

	adc_event++;
	
	ADC_read();
	check_faults();
	motor_control();	
}

void ADC_callback_add(void) {

	ADC_read();
	motor_control_add();	
}
